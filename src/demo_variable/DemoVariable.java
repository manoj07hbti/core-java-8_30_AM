package demo_variable;

public class DemoVariable {

    public static void main(String[] args) {

   // SYNTAX :  DataType varName=value;

        String name="RAJ"; //  value always will be in " "

        System.out.println("Printing name variable " +name);

        String cityName="DELHI";

        System.out.println("Printing city variable " +cityName);

        int age=21;
        System.out.println("Printing age :" +age);

        int marks=76;

        System.out.println("Printing marks " +marks);

        double price=354.67;
        System.out.println("Printing price " +price);

        double percentage=79.25;
        System.out.println("Printing percentage " +percentage);

    }
}
