package conditional_statements;

public class EvenOdd {

    public static void main(String[] args) {

        int number=7;

        if (number%2 == 0){

            System.out.println("Number is even number "+number);
        }

        if (number%2 !=0){

            System.out.println("Number is ODD number "+number);
        }

    }
}
